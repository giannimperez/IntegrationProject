import java.util.Scanner;

public class DataTypes {

  public static void main(String user) {
    System.out.println("_____Data Types_____");
    System.out.println("In the Java programming language, there are 4 main data types.");
    System.out.println(
        "A data type is a classification that specifies the type of value a variable has.");
    System.out.println();
    System.out.println("These data types include:");
    System.out.println("boolean: true or false.              i.e. true");
    System.out.println("int: any whole number.               i.e. 4");
    System.out.println("double: any decimal number.          i.e. 4.0");
    System.out.println("String: any alphanumeric characters. i.e. dog");
    System.out.println();
    System.out.println(
        "The boolean data type is used for basic situations in which you only need a true or false value.");
    System.out.println(
        "The int data type is used to hold a number value for something that doesn't typically involve complex math.");
    System.out.println("The double data type is used for more complex math involving fractions.");
    System.out.println(
        "The String data type is used to display print, everything from single characters to full sentences of text. Your own name, " + user + " is an example of a String.");
    System.out.println();
    
  }
  
}